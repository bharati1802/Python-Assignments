intVal = 10
floatVal = 5.5
stringVal = "Python"

print("Original values:")
print("intVal =", intVal, "Type:", type(intVal))
print("floatVal =", floatVal, "Type:", type(floatVal))
print("stringVal =", stringVal, "Type:", type(stringVal))

sum_result = intVal + floatVal
mul_result = intVal * floatVal
div_result = floatVal / intVal
str_mul_result = stringVal * 2

print("intVal + floatVal =", sum_result, "Type:", type(sum_result))
print("intVal * floatVal =", mul_result, "Type:", type(mul_result))
print("floatVal / intVal =", div_result, "Type:", type(div_result))
print("stringVal * 2 =", str_mul_result, "Type:", type(str_mul_result))
