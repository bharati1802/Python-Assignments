x = 12
y = 10

print("x =", x, "(Binary:", bin(x)[2], ")")
print("y =", y, "(Binary:", bin(y)[2], ")")
print("x & y (Bitwise AND):", x & y)
print("x | y (Bitwise OR):", x | y)
print("x ^ y (Bitwise XOR):", x ^ y)
print("~x (Bitwise NOT):", ~x)
print("x << 2 (Left shift by 2):", x << 2)
print("x >> 2 (Right shift by 2):", x >> 2)
